public class OperationsInTheCalculator {
    public static double sum(double number, double number2) {
        return number + number2;
    }

    public static double minus(double number, double number2) {
        return number - number2;
    }

    public static double multiplication(double number, double number2) {
        return number * number2;
    }

    public static double division(double number, double number2) {
        return number / number2;
    }

    public static double divisionWithRemainder(double number, double number2) {
        return number % number2;
    }
}
